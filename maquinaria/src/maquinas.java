
public class maquinas {
    private  String maquina; 
    private String tipoMaq;
    private String canMaquina;
    private int cosMaquina;

    /**
     * @return the maquina
     */
    public String getMaquina() {
        return maquina;
    }

    /**
     * @param maquina the maquina to set
     */
    public void setMaquina(String maquina) {
        this.maquina = maquina;
    }

    /**
     * @return the tipoMaq
     */
    public String getTipoMaq() {
        return tipoMaq;
    }

    /**
     * @param tipoMaq the tipoMaq to set
     */
    public void setTipoMaq(String tipoMaq) {
        this.tipoMaq = tipoMaq;
    }

    /**
     * @return the canMaquina
     */
    public String getCanMaquina() {
        return canMaquina;
    }

    /**
     * @param canMaquina the canMaquina to set
     */
    public void setCanMaquina(String canMaquina) {
        this.canMaquina = canMaquina;
    }

    /**
     * @return the cosMaquina
     */
    public int getCosMaquina() {
        return cosMaquina;
    }

    /**
     * @param cosMaquina the cosMaquina to set
     */
    public void setCosMaquina(int cosMaquina) {
        this.cosMaquina = cosMaquina;
    }
            
}
