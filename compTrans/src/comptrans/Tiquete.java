
package comptrans;

public class Tiquete {
    private int numeroViaje; 
    private String  fecha;
    private String  hora;
    private String nomPas;
    private String cos;

    /**
     * @return the numeroViaje
     */
    public int getNumeroViaje() {
        return numeroViaje;
    }

    /**
     * @param numeroViaje the numeroViaje to set
     */
    public void setNumeroViaje(int numeroViaje) {
        this.numeroViaje = numeroViaje;
    }

    /**
     * @return the fecha
     */
    public String getFecha() {
        return fecha;
    }

    /**
     * @param fecha the fecha to set
     */
    public void setFecha(String fecha) {
        this.fecha = fecha;
    }

    /**
     * @return the hora
     */
    public String getHora() {
        return hora;
    }

    /**
     * @param hora the hora to set
     */
    public void setHora(String hora) {
        this.hora = hora;
    }

    /**
     * @return the nomPas
     */
    public String getNomPas() {
        return nomPas;
    }

    /**
     * @param nomPas the nomPas to set
     */
    public void setNomPas(String nomPas) {
        this.nomPas = nomPas;
    }

    /**
     * @return the cos
     */
    public String getCos() {
        return cos;
    }

    /**
     * @param cos the cos to set
     */
    public void setCos(String cos) {
        this.cos = cos;
    }
    
    
}
